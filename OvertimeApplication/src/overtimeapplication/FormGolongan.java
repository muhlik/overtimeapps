    private void Baru(){
        btnSave.setText("Save");
        txtKodeGol.requestFocus();
        txtKodeGol.setText("");
        txtNamaGol.setText("");

        try {
            Class.forName(KoneksiDatabase.driver);
            java.sql.Connection c = DriverManager.getConnection(KoneksiDatabase.database, KoneksiDatabase.user, KoneksiDatabase.pass);
            Statement s = c.createStatement();
            String sql = "select * from golongan";
            ResultSet rs = s.executeQuery(sql);

            final String[] headers = {"Kode Golongan", "Nama Golongan"};
            rs.last();

            int n = rs.getRow();
            Object[][] data = new Object[n][2];
            int p = 0;
            rs.beforeFirst();
            while (rs.next()) {
                data[p][0] = rs.getString(1);
                data[p][1] = rs.getString(2);
                p++;
            }
            tblGolongan.setModel(new DefaultTableModel(data, headers));
            tblGolongan.setAlignmentX(CENTER_ALIGNMENT);
}
catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Koneksi, Ada Kesalahan.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
    private void Simpan() {
        if (txtKodeGol.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi Kode Golongan Pegawai !!!", "Peringatan", JOptionPane.ERROR_MESSAGE);
            txtKodeGol.requestFocus();
        } else if (txtNamaGol.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama Golongan Harus Diisi !!!", "Peringatan", JOptionPane.ERROR_MESSAGE);
            txtNamaGol.requestFocus();
        } else {
            try {
                Class.forName(KoneksiDatabase.driver);
                java.sql.Connection c = DriverManager.getConnection(KoneksiDatabase.database, KoneksiDatabase.user, KoneksiDatabase.pass);
                Statement s = c.createStatement();
                String sql;

                if (btnSave.getText().equals("Save")) {
                    sql = "insert into golongan values ('";
                    sql += txtKodeGol.getText() + "','";
                    sql += txtNamaGol.getText() + "')";
                } else {
                    sql = "update golongan set nama_gol = '";
                    sql += txtNamaGol.getText() + "'";
                    sql += "where kd_gol = '" + txtKodeGol.getText() + "'";
                }
                s.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!!!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Disimpan, Data Harus Lengkap !!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
            Baru();
        }

    }
